package datagenerator.exceptions;

public class DataGeneratorException extends Exception {
	
	/**
	 * Eclipse Generated Serial Version UID
	 */
	private static final long serialVersionUID = -8094601251579976030L;

	public DataGeneratorException() {
		super();
	}
	
	public DataGeneratorException(String msg) {
		super(msg);
	}
	
	public DataGeneratorException(Throwable throwable) {
		super(throwable);
	}
	
	public DataGeneratorException(String msg, Throwable throwable) {
		super(msg, throwable);
	}
}
